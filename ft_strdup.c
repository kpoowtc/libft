#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		size;
	char	*new;

	i = 0;
	size = 0;
	while (s1[size])
		size++;
	if (!((new = malloc(sizeof(char) * (size + 1)))))
		return (NULL);
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
