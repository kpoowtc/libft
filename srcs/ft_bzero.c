#include "./../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*buffer;
	size_t	i;

	buffer = s;
	i = 0;

	if (n == 0)
	{
		i = i + 1;
	}
	while (i < n)
	{
		buffer[i] = 0;
		i++;
	}
}
